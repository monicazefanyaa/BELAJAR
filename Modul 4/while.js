// Contoh 1: Perulangan dengan kondisi
let counter = 0;
while (counter < 5) {
    console.log("Iterasi ke-" + (counter + 1));
    counter++;
}

// Contoh 2: Perulangan dengan array
const fruits = ["Apel", "Jeruk", "Pisang", "Mangga"];
let index = 0;
while (index < fruits.length) {
    console.log("Buah: " + fruits[index]);
    index++;
}
